package pl.edu.pg.eti.dragondestiny.playedgame;

/**
 * A set of static properties of games.
 */
public class PlayedGameProperties {

    /**
     * Number of trophies needed to get strength points increase.
     */
    public static Integer numberOfTrophiesToGetPoint = 5;

    /**
     * Value by what the trophies increase the strength.
     */
    public static Integer trophiesPointIncrease = 1;

    /**
     * Number of cards player can have on hand.
     */
    public static Integer numberOfCardsOnHand = 5;

    /**
     * Low bound of dice roll.
     */
    public static Integer diceLowerBound = 1;

    /**
     * High bound of dice roll.
     */
    public static Integer diceUpperBound = 6;

    /**
     * ID of the moin boss.
     */
    public static Integer bossID = 9;

    /**
     * ID of a field on which boss stands.
     */
    public static Integer bossFieldID = 17;

    /**
     * ID of the bridge guardian (mini boss).
     */
    public static Integer guardianID = 8;

    /**
     * ID of a field on which bridge guardian stands.
     */
    public static Integer guardianFieldID = 16;
}
